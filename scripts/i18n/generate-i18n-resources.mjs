import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { basename, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const DEFAULT_ROOT = 'src/i18n/locales';
const DEFAULT_OUTPUT = 'src/i18n/resources.generated.ts';
const scriptPath = fileURLToPath(import.meta.url);
const DEFAULT_NAMESPACE_ORDER = [
  'app',
  'components',
  'landing',
  'settings',
  'photo-grid',
  'recycle-bin',
  'cleanup',
  'notifications',
  'recognition',
];

export function readJson(filePath) {
  return JSON.parse(readFileSync(filePath, 'utf8'));
}

export function listNamespaces(root) {
  const namespaces = new Set();
  for (const locale of ['zh', 'en']) {
    for (const entry of readdirSync(join(root, locale), { withFileTypes: true })) {
      if (entry.isFile() && entry.name.endsWith('.json')) {
        namespaces.add(basename(entry.name, '.json'));
      }
    }
  }

  return [
    ...DEFAULT_NAMESPACE_ORDER.filter((namespace) => namespaces.has(namespace)),
    ...[...namespaces].filter((namespace) => !DEFAULT_NAMESPACE_ORDER.includes(namespace)).sort(),
  ];
}

export function loadResources(root, namespaces) {
  const resources = {};
  for (const locale of ['zh', 'en']) {
    resources[locale] = {};
    for (const namespace of namespaces) {
      resources[locale][namespace] = readJson(join(root, locale, `${namespace}.json`));
    }
  }

  return resources;
}

export function generateTypeScript(resources, namespaces) {
  return `// This file is generated by scripts/i18n/generate-i18n-resources.mjs.
// Edit src/i18n/locales/**/*.json, then regenerate.

export const I18N_RESOURCE_NAMESPACES = ${JSON.stringify(namespaces, null, 2)} as const;

export const APP_I18N_RESOURCES = ${JSON.stringify(resources, null, 2)} as const;

export type I18nResourceNamespace = (typeof I18N_RESOURCE_NAMESPACES)[number];
export type AppI18nResources = typeof APP_I18N_RESOURCES;
`;
}

function normalizeGenerated(content) {
  return content.endsWith('\n') ? content : `${content}\n`;
}

function parseArgs(argv) {
  const options = {
    root: DEFAULT_ROOT,
    output: DEFAULT_OUTPUT,
    check: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '--root') {
      options.root = argv[++index];
    } else if (arg === '--output') {
      options.output = argv[++index];
    } else if (arg === '--check') {
      options.check = true;
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }

  return options;
}

export function generateI18nResourceOutput({ root = DEFAULT_ROOT, output = DEFAULT_OUTPUT, check = false } = {}) {
  const resolvedRoot = resolve(process.cwd(), root);
  const resolvedOutput = resolve(process.cwd(), output);
  const namespaces = listNamespaces(resolvedRoot);
  const resources = loadResources(resolvedRoot, namespaces);
  const generated = normalizeGenerated(generateTypeScript(resources, namespaces));

  if (check) {
    if (!existsSync(resolvedOutput)) {
      throw new Error(`Generated i18n resource output is missing: ${output}`);
    }

    const actual = readFileSync(resolvedOutput, 'utf8');
    if (actual !== generated) {
      throw new Error(
        `Generated i18n resource output is stale: ${output}\nRun: node scripts/i18n/generate-i18n-resources.mjs`,
      );
    }
  } else {
    writeFileSync(resolvedOutput, generated);
  }

  return {
    namespaces,
    output,
  };
}

function main() {
  const options = parseArgs(process.argv.slice(2));
  const result = generateI18nResourceOutput(options);
  const action = options.check ? 'Verified' : 'Generated';
  console.log(`${action} ${result.output} from ${result.namespaces.length} i18n namespaces.`);
}

if (process.argv[1] === scriptPath) {
  try {
    main();
  } catch (error) {
    console.error(error instanceof Error ? error.message : error);
    process.exitCode = 1;
  }
}
